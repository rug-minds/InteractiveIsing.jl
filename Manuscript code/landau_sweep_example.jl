###############################################################################
# Base parameters shared by all Landau variants
###############################################################################

landau_base = MT.ManuscriptParams(;
    outdir = raw"D:\Code\data\Manuscript\Landau_sweep_demo",
    xL = 40,
    yL = 40,
    zL = 10,
    JIsing = 1,
